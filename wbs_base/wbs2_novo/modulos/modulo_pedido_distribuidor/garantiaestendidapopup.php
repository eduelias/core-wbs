        <td><b>1) O que é a Garantia Estendida Ipasoft<br>
        </b>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;A Garantia
          Estendida Ipasoft é um serviço opcional, que cobre os custos referentes
          a substituição de peças componentes do microcomputador, bem como da
          mão de obra de substituição das mesmas, por um período adicional de
          12 meses além do período de garantia convencional dos microcomputadores
          Ipasoft, desde que a origem do defeito não seja mau uso, queda, surtos
          elétricos ou outras causas que estejam discriminadas nas cláusulas
          no contrato de garantia.<br>
<br>
<b>2) Como orçar e vender a Garantia Estendida Ipasoft<br>
</b>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;No momento da simulação
do preço do microcomputador, o cliente pode optar por contratar a Garantia Estendida.
O valor adicional é inserido no custo do equipamento, e distribuído na forma
de pagamento que for simulada para o cliente.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Nem todos os produtos que compõe
o micro são cobertos pela garantia estendida, tais como mouses, teclados, caixas
de som e outros. Os produtos que possuem a possibilidade de contratação da garantia
estendida estão marcados com uma estrela "<img src="images/estrela.gif" height="13" width="13">" na coluna "gar (mês)" nas telas
da confecção do carrinho de compras do WBS.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;No pedido, ao optar pela garantia
estendida, o contrato personalizado é gerado
na hora, discriminando a extensão dos prazos de garantia de todas as peças do
computador escolhido. A partir daí, a opção pela garantia estendida estará em
nosso banco de dados, proporcionando um atendimento diferenciado ao nosso cliente.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;O
valor do contrato de Garantia Estendida Ipasoft depende da configuração do
equipamento que estiver sendo vendido.<br>
<br>
<b>3) Comissões<br>
</b>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;O Valor do serviço de garantia estendida soma-se ao valor
do orçamento. Conseqüentemente,
a comissão incide também sobre o valor da garantia estendida, no mesmo percentual
combinado para as peças.<br>
<br>
<b>4) Observação Importante:<br>
</b>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;A Garantia Estendida Ipasoft restringe-se exclusivamente
aos computadores e tem efeito nas peças que possuem a estrela "<img src="images/estrela.gif" height="13" width="13">", não podendo
ser contratada para peças
avulsas, acessórios e periféricos.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;A Garantia Estendida Ipasoft só poderá ser
contratada no momento da emissão e
aceite do pedido de venda. Não existe possibilidade de contratação posterior à emissão
do pedido.<br></td>
